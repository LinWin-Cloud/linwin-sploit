function set_white()
{
    //set color white
    var body = document.getElementById("body") ;
    body.style.color = 'white' ;

    var rwl = document.getElementById('rwl') ;
    rwl.style.color = 'white' ;
    rwl.style.border = 'white solid 2px' ;

    var running = document.getElementById('running') ;
    running.style.border = 'white solid 3px'

    var setth = document.getElementById('setth') ;
    setth.style.border = 'white solid 2px'

    var viewth = document.getElementById('viewth') ;
    viewth.style.border = 'white solid 2px' ;

    var helpth = document.getElementById('helpth') ;
    helpth.style.border = 'white solid 2px' ;

    var aboutth = document.getElementById('aboutth') ;
    aboutth.style.border = 'white solid 2px' ;

    var bottomdiv =document.getElementById('bottomdiv') ;
    bottomdiv.style.border = 'white solid 2px' ;
}
